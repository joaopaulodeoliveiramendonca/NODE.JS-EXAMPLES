// Define uma função chamada `sum` que recebe dois parâmetros `a` e `b`
function sum(a, b) { 
  // A função imprime no console a soma de `a` e `b`
  console.log(a + b);
}

// Exporta a função `sum` como a exportação padrão deste módulo
// Isso permite que outros arquivos importem e utilizem a função
export default sum; 
